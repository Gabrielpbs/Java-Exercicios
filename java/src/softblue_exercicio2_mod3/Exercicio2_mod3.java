//Imprima todos os números inteiros de 10 a 25 utilizando uma estrutura de repetição.

package softblue_exercicio2_mod3;

public class Exercicio2_mod3 {
	
	public static void main(String[] args) {
		
		int numeroInteiro = 10;
		
		while ((numeroInteiro >= 10 ) && (numeroInteiro <= 25)) {
			
			System.out.println(numeroInteiro);
			numeroInteiro ++;
		}
	}
}
